class Pago {
    var colegiatura: String
    var referencia: String
    var fecha: String
    var importe: String
    
    init(colegiatura: String, referencia: String, fecha: String, importe: String) {
        self.colegiatura = colegiatura
        self.referencia = referencia
        self.fecha = fecha
        self.importe = importe
    }
    
}
